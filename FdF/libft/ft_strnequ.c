#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;
	size_t	j;

	i = 0;
	j = -1;
	if (s1 == '\0' || s2 == '\0')
		return (0);
	while ((s1[i] || s2[i]) && ++j < n)
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	return (1);
}
